package text;

public class Colors {
    //ANSI colors
    public static final String RESET = "\u001B[0m";
    public static final String RED = "\u001B[31m";  // Used for damage
    public static final String GREEN = "\u001B[32m";    // Used for hp
    public static final String YELLOW = "\u001B[33m";
    public static final String BLUE = "\u001B[34m";
    public static final String PURPLE = "\u001B[35m";
    public static final String CYAN = "\u001B[36m";
    public static final String GRAY = "\u001B[37m";
    public static final String MAGENTA = "\u001B[35m";

    public static final String HIGH_RED = "\u001B[0;91m";
    public static final String HIGH_GREEN = "\u001B[0;92m";
    public static final String HIGH_YELLOW = "\u001B[0;93m";
    public static final String HIGH_BLUE = "\u001B[0;94m";
    public static final String HIGH_PURPLE = "\u001B[0;95m";
    public static final String HIGH_CYAN = "\u001B[0;96m";
}
